package ConstrutoresEInstancias;
/* classe Usuario representa um usuario com nome e idade
possui dois atributos uma String pra nome e int pra idade
um construtor que inicializa esses dois valores quando cria o objeto
 */
public class Usuario {
    String nome;
    int idade;

    public Usuario(String n, int i){
        nome = n;
        idade = i;
    }
}
